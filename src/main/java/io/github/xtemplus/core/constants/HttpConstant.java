package io.github.xtemplus.core.constants;

/**
 * HTTP响应状态码常量类
 * 定义了系统中使用的所有HTTP状态码及其含义
 * 包括标准HTTP状态码和自定义业务状态码
 *
 * @author template
 */
public interface HttpConstant {
    /**
     * 操作成功
     * 请求已成功，请求所希望的响应头或数据体将随此响应返回
     */
    int SUCCESS = 200;

    /**
     * 对象创建成功
     * 请求已经被实现，而且有一个新的资源已经依据请求的需要而建立
     */
    int CREATED = 201;

    /**
     * 请求已经被接受
     * 请求已经进入后台排队（异步任务）
     */
    int ACCEPTED = 202;

    /**
     * 操作已经执行成功，但是没有返回数据
     * 服务器成功处理了请求，但不需要返回任何实体内容
     */
    int NO_CONTENT = 204;

    /**
     * 资源已被移除
     * 请求的资源已永久移动到新位置，并且将来任何对此资源的引用都应该使用新的URI
     */
    int MOVED_PERM = 301;

    /**
     * 重定向
     * 当前请求应该使用另一个URL重新发送
     */
    int SEE_OTHER = 303;

    /**
     * 资源没有被修改
     * 所请求的资源未修改，服务器返回此状态码时，不会返回任何资源
     */
    int NOT_MODIFIED = 304;

    /**
     * 参数列表错误（缺少，格式不匹配）
     * 客户端请求的语法错误，服务器无法理解
     */
    int BAD_REQUEST = 400;

    /**
     * 未授权
     * 请求要求用户的身份认证，常见于登录失败
     */
    int UNAUTHORIZED = 401;

    /**
     * 访问受限，授权过期
     * 服务器理解请求客户端的请求，但是拒绝执行此请求（权限不够）
     */
    int FORBIDDEN = 403;

    /**
     * 资源，服务未找到
     * 服务器无法根据客户端的请求找到资源（网页、接口等）
     */
    int NOT_FOUND = 404;

    /**
     * 不允许的HTTP方法
     * 客户端请求中的方法被禁止（如：不允许POST只能GET）
     */
    int BAD_METHOD = 405;

    /**
     * 资源冲突，或者资源被锁定
     * 请求的资源与现有资源发生冲突，例如重复创建相同资源
     */
    int CONFLICT = 409;

    /**
     * 不支持的数据，媒体类型
     * 服务器无法处理请求附带的媒体格式（如：不支持的文件类型）
     */
    int UNSUPPORTED_TYPE = 415;

    /**
     * 系统内部错误
     * 服务器内部错误，无法完成请求，需要查看服务器错误日志
     */
    int ERROR = 500;

    /**
     * 接口未实现
     * 服务器不支持请求的功能，无法完成请求（接口还未开发完成）
     */
    int NOT_IMPLEMENTED = 501;

    /**
     * 系统警告消息
     * 服务器正常处理请求，但需要警示用户（自定义状态码）
     */
    int WARN = 601;
}
